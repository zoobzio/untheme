import type { Assembled } from "./types";

import { serialize } from "./util";

/**
 * Renders the generated `untheme.config.ts`: a header marking it generated,
 * the configuration literal as the default export, and the catalog layers as
 * a named `themes` export — the config carries only the theme and the boot
 * selection; serving the catalog is the consumer's concern. The consumer's
 * TypeScript infers the contract from the config literal and proves the
 * layers against it through the `satisfies` clause — full type safety lands
 * there, not here.
 */
export const emit = (assembled: Assembled, source: string): string => {
  const config = serialize(
    {
      theme: assembled.theme,
      input: assembled.input,
    },
    "",
  );
  const themes = serialize(assembled.themes, "");
  return [
    "/**",
    ` * Generated by @untheme/terrazzo from ${source}.`,
    " * Do not edit by hand — regenerate from the token sources.",
    " */",
    `import type { Layer } from "untheme";`,
    "",
    `import { defineUnthemeConfig } from "untheme/config";`,
    "",
    `const config = defineUnthemeConfig(${config});`,
    "",
    `export const themes = ${themes} satisfies Record<`,
    "  string,",
    "  Layer<typeof config.theme>",
    ">;",
    "",
    "export default config;",
    "",
  ].join("\n");
};
